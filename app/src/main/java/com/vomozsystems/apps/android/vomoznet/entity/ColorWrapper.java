import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class ColorWrapper  {

    private Long id;

    @SerializedName("name")
    @Expose
    private String name;

    private String fullName;

    @SerializedName("address")
    @Expose
    private String address;

    @SerializedName("cardId")
    @Expose
    private Long cardId;

    private String merchantIdCode;

    @SerializedName("phone")
    @Expose
    private String phone;

    @SerializedName("email")
    @Expose
    private String email;

    @SerializedName("tags")
    @Expose
    private String tags;

    @SerializedName("url")
    @Expose
    private String url;

    private String logoUrl;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTags() {
        return tags;
    }

    public void setTags(String tags) {
        this.tags = tags;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    /**
     *
     * @param name
     * The name
     */
    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public Long getCardId() {
        return cardId;
    }

    public void setCardId(Long cardId) {
        this.cardId = cardId;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getMerchantIdCode() {
        return merchantIdCode;
    }

    public void setMerchantIdCode(String merchantIdCode) {
        this.merchantIdCode = merchantIdCode;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getLogoUrl() {
        return logoUrl;
    }

    public void setLogoUrl(String logoUrl) {
        this.logoUrl = logoUrl;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof ColorWrapper)) return false;

        ColorWrapper that = (ColorWrapper) o;

        if (!getId().equals(that.getId())) return false;
        return getCardId().equals(that.getCardId());
    }

    @Override
    public int hashCode() {
        int result = getId().hashCode();
        result = 31 * result + getCardId().hashCode();
        return result;
    }
}